//Write a C program to calculate the area of a circle and the volume of a cylinder using given radius and height.
#include<stdio.h>
int main(){
    int r = 6;
    int height = 10;
    printf("The area of circle is %d is %f\n", 3.14*r*r);
    printf("The volume of cylinder is %d is %f\n", 3.14*r*r*height);
    
    return 0;
}